                    dopo la fine del primo manga Inazuma Eleven, e come quest'ultimo è realizzato da Ten'ya Yabuno 
                    e pubblicato da Shogakukan sulla rivista CoroCoro Comic. La trama del manga Inazuma Eleven GO copre 
                    anche gli eventi della successiva serie anime Inazuma Eleven GO: Chrono Stone, a partire dal terzo volume, 
                    e della penultima serie, Inazuma Eleven GO: Galaxy, solo nel settimo volume. Il manga è terminato il 15 marzo 2014. 
                    In Italia il manga è edito dalla J-Pop e la sua pubblicazione è iniziata il 25 maggio 2016.
                    <br />
                    L'anime è stato trasmesso in Italia sul canale Disney XD: i primi 20 episodi sono andati in onda dal 1º al 23 luglio 2013, gli altri dal 14 ottobre al 19 novembre 2013. In chiaro è stato trasmesso per la prima volta dal 6 al 21 luglio 2014 sul canale Rai Gulp.
